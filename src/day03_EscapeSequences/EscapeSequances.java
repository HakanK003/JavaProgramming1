package day03_EscapeSequences;
/*
Escape sequances:MUST be given with double quote
          \n: starts a new line
          \t: paragraph space = (tab)
          \\: single back slash = \ if you want x2 you have to do x4 \\\\ (you can't use multiple times)
          \": single double quote = "
 */
public class EscapeSequances {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("---------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("---------------");

        System.out.println("\tJava is Cool Programming Language");





    }





}
