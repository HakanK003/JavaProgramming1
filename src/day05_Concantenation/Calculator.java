package day05_Concantenation;

public class Calculator {

    public static void main(String[] args) {

        int firstNumber = 100;
        int secondNumber = 50;

        // 100 + 50 = 150

        System.out.println(firstNumber + " + " + secondNumber + " = " + (firstNumber+secondNumber));
                                                                        //ya boyle ya da yeni int acmak lazim
        // 100 - 50 = 50

        System.out.println(firstNumber + " - " + secondNumber + " = " + (firstNumber-secondNumber));

        //100 * 50 = 5000

        System.out.println(firstNumber + " * " + secondNumber + " = " + (firstNumber*secondNumber));


    }
}
